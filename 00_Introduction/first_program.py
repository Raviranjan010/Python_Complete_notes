# This is a "Comment". Lines starting with '#' are ignored by Python.
# They are used to explain code to humans.

# 1. The Print Function
# The print() function is used to display output to the console/screen.
print("Hello, World!") 

# ---------------------------------------------------------

# 2. Printing Numbers
# You can print numbers directly without quotes.
print(100)
print(3.14)

# ---------------------------------------------------------

# 3. Printing Multiple Items
# You can print multiple items separated by a comma.
# Python automatically adds a space between them.
print("My age is", 25)

# ---------------------------------------------------------

# 4. Edge Case: Quotes inside Quotes
# If you want to print a quote symbol, use different quotes for the wrapper.
print("He said, 'Python is awesome!'")  # Double quotes outside, single inside
print('She said, "I agree!"')          # Single quotes outside, double inside

# ---------------------------------------------------------
# INTERVIEW TRIVIA:
# Python 2 used to use `print "Hello"` (no parentheses).
# Python 3 requires parentheses: `print("Hello")`.
